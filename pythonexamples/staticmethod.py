class DurgaMath():
    @staticmethod
    def add(a,b):
        print('The sum',(a+b))
    @staticmethod
    def product(a,b):
        print('The product',(a*b))
    @staticmethod
    def average(a,b):
        print('The average',(a+b)/2)
DurgaMath.add(10,20)
DurgaMath.product(10,20)
DurgaMath.average(10,20)




